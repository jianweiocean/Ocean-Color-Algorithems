function [Rrs412, Rrs443] = blue_band_correction(test_Rrs, test_lambda)
% Blue-band correction model, v2.0
% Estimate Rrs(412) & Rrs(443) from Rrs(488),Rrs(551) and Rrs(670)
% INPUTS:
%       test_Rrs:        matrix of Satellite Rrs spectra at three bands, each row representing an individual spectrum at four visible bands;%       
%       test_lambda:     wavelength = 488, 551, 670 nm
% OUTPUT:
%       Rrs412 &  Rrs443:    
% Sub-functions:
%       standard_Rrs_class
%
% *********************************************************************
% Jianwei Wei, NOAA/STAR Marince Ecosystems and Climatology Branch
% Jianwei.Wei@noaa.gov
%
% created January 15, 2020
%

%%% standard Rrs class
[ref_nRrs, ref_lambda] = standard_Rrs_class();

%%% make sure test_lambda = ref_lambda
lambda_diff = sum(test_lambda - ref_lambda(3:5));
if lambda_diff~=0
    error('Input Rrs spectra have different wavelengths than the reference data!')
end



% keep the original value
test_Rrs_orig = test_Rrs;

[refRow,refCol]=size(ref_nRrs);

%% normalization
[inRow, inCol] = size(test_Rrs);

% transform spectrum to column, inCol*inRow
test_Rrs = test_Rrs';
test_Rrs_orig = test_Rrs_orig';

% inCol*inRow
nRrs_denom=sqrt(nansum(test_Rrs.^2));
nRrs_denom = repmat(nRrs_denom,[inCol,1]);
nRrs = test_Rrs./nRrs_denom;  

% SAM input, inCol*inRow*refRow 
test_Rrs2 = repmat(test_Rrs_orig,[1,1,refRow]);

%for ref Rrs, inCol*refRow*inRow 
test_Rrs2p = permute(test_Rrs2,[1,3,2]);

% inCol*inRow*refRow  
nRrs2_denom=sqrt(nansum(test_Rrs2.^2));
nRrs2_denom = repmat(nRrs2_denom,[inCol,1]);
nRrs2 = test_Rrs2./nRrs2_denom;  
% inCol*refRow*inRow  
nRrs2 = permute(nRrs2,[1,3,2]);



%%
ref_nRrs2 = ref_nRrs(:,3:5)';

% inCol*refRow*inRow 
ref_nRrs2 = repmat(ref_nRrs2,[1,1,inRow]);


%% Classification 
%%%% calculate the Spectral angle mapper
% inCol*refRow*inRow 
cos_denom=sqrt(nansum(ref_nRrs2.^2).*nansum(nRrs2.^2));
cos_denom = repmat(cos_denom,[inCol,1]);
cos = (ref_nRrs2.*nRrs2)./cos_denom; 
% 1*refRow*inRow 
cos = sum(cos);
% refRow*inRow
cos = permute(cos,[2,3,1]);

% 1*inRow
[maxCos,clusterID] = max(cos);
posClusterID = isnan(maxCos);


%% derive Rrs412

ref_nRrs3 = ref_nRrs(clusterID,:);
ref_nRrs3 = ref_nRrs3';

Rrs412(:,1) = sqrt(nansum(test_Rrs.^2).*ref_nRrs3(1,:).^2./(1-ref_nRrs3(1,:).^2));


% size(test_Rrs)
% size(ref_nRrs3)

output1 = sqrt( nansum(test_Rrs.^2)./(1 - ref_nRrs3(1,:).^2 - ref_nRrs3(2,:).^2 ) ) .* ref_nRrs3(1,:);
output2 = sqrt( nansum(test_Rrs.^2)./(1 - ref_nRrs3(1,:).^2 - ref_nRrs3(2,:).^2 ) ) .* ref_nRrs3(2,:);

Rrs412 = output1';
Rrs443 = output2';



end





